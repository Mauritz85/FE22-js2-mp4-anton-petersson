export const add = (term1: number, term2:number):number => {
    return +term1 + +term2
 }
 
 export const subtract = (term1:number, term2:number):number => {
    return term1 - term2
 }
 
 export const multiply = (factor1:number, factor2:number):number => {
    return factor1 * factor2
 }

 export const divide = (numerator: number, denominator: number):number => {
    return numerator / denominator
 }

